let nombre= "Juan";
let edad= 25;
console.log("Hola soy  " + nombre + " tengo :  " + edad + " años"); // Hola soy Juan tengo :25 años 

//Puedo concadenar usando comas, en vez de espacios, el resultado es el mismo pero sin necesidad de usar el signo + para concatenar
console.log("Hola soy ", nombre, " tengo : ", edad, " años"); // Hola soy  Juan  tengo :  25  años


console.log(`Hola soy ${nombre} tengo : ${edad} años`); // Hola soy Juan tengo : 25 años
